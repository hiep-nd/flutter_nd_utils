class NDPpis {
  final NDIModel model;
  final String device;
  final Tuple2<int, int> version;
  final String name;
  final double ppi;
  NDPpis(
    this.model,
    this.device,
    this.version,
    this.name,
    double widthByPoints,
    double heightByPoints,
    double screenDiagonalSizeByInches,
  ) : ppi = sqrt(widthByPoints * widthByPoints +
                heightByPoints * heightByPoints) /
            screenDiagonalSizeByInches;
}

